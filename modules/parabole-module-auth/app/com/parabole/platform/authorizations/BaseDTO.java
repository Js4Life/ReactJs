package com.parabole.platform.authorizations;

import org.apache.commons.lang3.builder.CompareToBuilder;
import org.apache.commons.lang3.builder.EqualsBuilder;
import org.apache.commons.lang3.builder.HashCodeBuilder;
import org.apache.commons.lang3.builder.ToStringBuilder;

import java.io.Serializable;

/**
 * Base Data Transfer Object (DTO) to be extended by all DTO.
 *
 * @author Subhasis Sanyal
 */
public abstract class BaseDTO implements Cloneable, Serializable {

    private static final long serialVersionUID = -6343301279206981492L;

    @Override
    public boolean equals(final Object o) {
        return EqualsBuilder.reflectionEquals(this, o);
    }

    @Override
    public int hashCode() {
        return HashCodeBuilder.reflectionHashCode(this);
    }

    @Override
    public String toString() {
        return ToStringBuilder.reflectionToString(this);
    }

    public int compareTo(final Object o) {
        return CompareToBuilder.reflectionCompare(this, o);
    }
}
